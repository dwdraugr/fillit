#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*res;
	size_t	check;

	check = 0;
	if (size == check - 1)
		return (NULL);
	res = ft_memalloc((size + 1) * sizeof(char));
	if (!res)
		return (NULL);
	return (res);
}
